/**
 * @author Sana Development
 *
 */
public class HttpThread<T> extends Thread {
	
	ResponseHandler<T> handler;
	HttpUriRequest request;
	HttpClient client;
	
	/**
	 * 
	 */
	public HttpThread(HttpUriRequest request) {
		this(new DefaultHttpClient(), request,  null);	
	}
	
	/**
	 * 
	 * @param request
	 * @param handler
	 */
	public HttpThread(HttpUriRequest request, ResponseHandler<T> handler) {
		this(new DefaultHttpClient(), request,handler);		
	}
	
	public HttpThread(HttpClient client, HttpUriRequest request, ResponseHandler<T> handler) {
		this.request = request;
		this.handler = handler;
		this.client = client;
	}
	
	/* (non-Javadoc)
	 * @see java.lang.Runnable#run()
	 */
	@Override
	public void run() {
		HttpResponse response = null;
		try {
			response = client.execute(request);
			if(handler != null)
				handler.handleResponse(response);
		} catch (ClientProtocolException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}
	}

}
