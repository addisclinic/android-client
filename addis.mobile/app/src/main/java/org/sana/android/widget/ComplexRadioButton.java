package org.sana.android.widget;

import android.content.Context;
import android.util.AttributeSet;
import android.widget.RadioButton;

/**
 * @author Sana Development
 *
 */
public class ComplexRadioButton extends RadioButton {

    
    public ComplexRadioButton(Context context) {
        this(context, null);
    }
    
    public ComplexRadioButton(Context context, AttributeSet attrs) {
        this(context, attrs, org.sana.R.attr.complexRadioButtonStyle);
    }

    public ComplexRadioButton(Context context, AttributeSet attrs, int defStyle) {
        super(context, attrs, defStyle);
    }

    /**
     * {@inheritDoc}
     * <p>
     * If the radio button is already checked, this method will not toggle the radio button.
     */
    @Override
    public void toggle() {
        // we override to prevent toggle when the radio is already
        // checked (as opposed to check boxes widgets)
        if (!isChecked()) {
            super.toggle();
        }
    }

}
